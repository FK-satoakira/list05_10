package list05_02;

public class Character {
	private String name;
	private int age;

	public Character(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	@Override
	public String toString() {
		return "Chara [name=" + name + ", age=" + age + "]";
	}
}
